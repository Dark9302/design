                            <h1>装修旺季促销建材家居景气指数回升</h1>
                            <div class="info">
							<span>
								2016-09-17 23:28:48
							</span>
                                <span>
								admin
							</span>
                                <span>
								<i class="icon wb-eye margin-right-5" aria-hidden="true"></i>197
							</span>
                            </div>
                        </div>
                        <div class="met-editor lazyload clearfix">
                            <div><p>
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 由中国建筑材料流通协会发布的全国建材家居景气指数BHI9月份为119.02，环比上升10.05%，同比上升1.35%。这符合业界预期。<br/>
                                    &nbsp;</p><p>
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 中国建筑材料流通协会副会长秦占学对此进行了解读："在全国房地产价格拐点隐隐欲现之时，BHI却出现了回升，这主要有三个原因促成。首先是九、十月份为装修旺季，加之各大建材家居市场使出浑身解数大力促销，致使BHI环比有较大升幅，十月份也许还会延续这一阵势，从BHI中的就业率指数环比上升看，也反映了大家为促销而增人加马；其次是全国建材家居市场今年在投资建设上也有较大发展，总营业面积不断扩大，一旦遇到好转会出现倍增效应，这是BHI同比也有所上升的主要原因；最后是房地产的刚性需求在经过一段等待后，一些区域房地产价格有所下降，使之得到了一定的释放，这种释放是阶段性的，房地产刚性需求的释放会带动建材家居业的上升。"<br/>
                                    &nbsp;</p><p>
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 对于BHI后期走势，他还指出"9月份BHI中人气指数环比升幅远小于销售能力指数的环比升幅，说明BHI上升趋势不可能维持太久，预计十一月、十二月还会降下来。"</p><div id="metinfo_additional"></div></div>
